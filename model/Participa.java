package model;

public class Participa {
	private String dni;
	private int partida_ID;
	
	
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public int getPartida_id() {
		return partida_ID;
	}
	public void setPartida_id(int partida_id) {
		this.partida_ID = partida_id;
	}
	
	
	
}
